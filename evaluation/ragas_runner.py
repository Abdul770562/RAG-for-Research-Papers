"""
evaluation/ragas_runner.py

Executes the complete modern RAGAS evaluation suite asynchronously using ascore().
Includes: Context Precision, Context Utilization, Context Recall, Faithfulness,
          Answer Relevancy, and Noise Sensitivity.
Uses a hybrid local embedding (all-MiniLM-L6-v2) and throttled cloud LLM setup.
"""

import json
import asyncio
from pathlib import Path
import pandas as pd

from ragas.metrics.collections import (
    ContextPrecision, 
    ContextUtilization, 
    ContextRecall,
    Faithfulness,
    AnswerRelevancy,
    NoiseSensitivity
)

from evaluation.evaluator_llm import EvaluatorLLM
from utils.logger import logger

# ==========================================================
# PATHS & CONFIGURATION
# ==========================================================
BASE_DIR = Path(__file__).resolve().parent.parent
EVAL_DATASET_FILE = BASE_DIR / "evaluation" / "outputs" / "evaluation_dataset.json"
METRICS_OUTPUT_FILE = BASE_DIR / "evaluation" / "outputs" / "ragas_metrics_results.json"

# Concurrency & pacing throttles to respect usage limits
MAX_CONCURRENT_REQUESTS = 1 
SAMPLE_LIMIT = 1
STEP_DELAY_SECONDS = 2.0


class RagasEvaluationRunner:
    """
    Manages loading datasets and executing the complete set of modern RAGAS metrics 
    asynchronously under a strict concurrency execution semaphore.
    """

    def __init__(self) -> None:
        logger.info("Initializing complete modern Asynchronous RAGAS Evaluation Suite...")
        self.evaluator_infra = EvaluatorLLM()
        self.llm = self.evaluator_infra.llm
        self.embeddings = self.evaluator_infra.embeddings
        
        # Instantiate all modern metrics collections
        self.context_precision_scorer = ContextPrecision(llm=self.llm)
        self.context_utilization_scorer = ContextUtilization(llm=self.llm)
        self.context_recall_scorer = ContextRecall(llm=self.llm)
        self.faithfulness_scorer = Faithfulness(llm=self.llm)
        self.answer_relevancy_scorer = AnswerRelevancy(llm=self.llm, embeddings=self.embeddings) # type: ignore
        self.noise_sensitivity_scorer = NoiseSensitivity(llm=self.llm)
        
        # Global semaphore to throttle outgoing requests
        self.semaphore = asyncio.Semaphore(MAX_CONCURRENT_REQUESTS)

    def load_dataset(self) -> list[dict]:
        """
        Loads the clean evaluation JSON array generated by build_evaluation_dataset.py.
        """
        if not EVAL_DATASET_FILE.exists():
            raise FileNotFoundError(
                f"Evaluation dataset missing at {EVAL_DATASET_FILE}. Please run build_evaluation_dataset.py first."
            )

        logger.info("Loading evaluation dataset from %s", EVAL_DATASET_FILE)
        with open(EVAL_DATASET_FILE, "r", encoding="utf-8") as f:
            full_data = json.load(f)
            
        sliced_data = full_data[:SAMPLE_LIMIT]
        logger.info("Sliced dataset down from %d to %d test rows.", len(full_data), len(sliced_data))
        return sliced_data

    async def evaluate_sample(self, record: dict, idx: int, total: int) -> dict:
        """
        Evaluates a single sample row across all 6 active metrics sequentially.
        """
        logger.info("[%d/%d] Queueing metrics for Question ID: %s", idx, total, record["id"])
        
        async with self.semaphore:
            # 1. Context Precision
            logger.info("[%d/%d] Processing Context Precision for ID: %s", idx, total, record["id"])
            precision_result = await self.context_precision_scorer.ascore(
                user_input=record["question"],
                reference=record["ground_truth"],
                retrieved_contexts=record["contexts"]
            )
            await asyncio.sleep(STEP_DELAY_SECONDS)

            # 2. Context Utilization
            logger.info("[%d/%d] Processing Context Utilization for ID: %s", idx, total, record["id"])
            utilization_result = await self.context_utilization_scorer.ascore(
                user_input=record["question"],
                response=record["answer"],
                retrieved_contexts=record["contexts"]
            )
            await asyncio.sleep(STEP_DELAY_SECONDS)

            # 3. Context Recall
            logger.info("[%d/%d] Processing Context Recall for ID: %s", idx, total, record["id"])
            recall_result = await self.context_recall_scorer.ascore(
                user_input=record["question"],
                reference=record["ground_truth"],
                retrieved_contexts=record["contexts"]
            )
            await asyncio.sleep(STEP_DELAY_SECONDS)

            # 4. Faithfulness
            logger.info("[%d/%d] Processing Faithfulness for ID: %s", idx, total, record["id"])
            faithfulness_result = await self.faithfulness_scorer.ascore(
                user_input=record["question"],
                response=record["answer"],
                retrieved_contexts=record["contexts"]
            )
            await asyncio.sleep(STEP_DELAY_SECONDS)

            # 5. Answer Relevancy
            logger.info("[%d/%d] Processing Answer Relevancy for ID: %s", idx, total, record["id"])
            relevancy_result = await self.answer_relevancy_scorer.ascore(
                user_input=record["question"],
                response=record["answer"]
            )
            await asyncio.sleep(STEP_DELAY_SECONDS)

            # 6. Noise Sensitivity
            logger.info("[%d/%d] Processing Noise Sensitivity for ID: %s", idx, total, record["id"])
            noise_result = await self.noise_sensitivity_scorer.ascore(
                user_input=record["question"],
                response=record["answer"],
                reference=record["ground_truth"],
                retrieved_contexts=record["contexts"]
            )
            
        # Compile final dictionary record payload
        enriched_record = record.copy()
        enriched_record["metrics"] = {
            "context_precision": getattr(precision_result, "value", float(precision_result)),
            "context_utilization": getattr(utilization_result, "value", float(utilization_result)),
            "context_recall": getattr(recall_result, "value", float(recall_result)),
            "faithfulness": getattr(faithfulness_result, "value", float(faithfulness_result)),
            "answer_relevancy": getattr(relevancy_result, "value", float(relevancy_result)),
            "noise_sensitivity": getattr(noise_result, "value", float(noise_result))
        }
        
        await asyncio.sleep(4.0)
        return enriched_record

    async def _run_lifecycle(self) -> None:
        try:
            records = self.load_dataset()
            logger.info("Beginning async execution on %d evaluation tokens...", len(records))
            
            evaluated_records = []
            
            for idx, record in enumerate(records, start=1):
                enriched = await self.evaluate_sample(record, idx, len(records))
                evaluated_records.append(enriched)

            # Convert to DataFrame to print summary stats
            df_final = pd.json_normalize(evaluated_records)
            
            logger.info("=== RAGAS Metric Summary (Complete 6-Metric Suite) ===")
            metrics_list = [
                "context_precision", "context_utilization", "context_recall", 
                "faithfulness", "answer_relevancy", "noise_sensitivity"
            ]
            for metric in metrics_list:
                col = f"metrics.{metric}"
                if col in df_final.columns:
                    logger.info("Mean %s: %.4f", metric.replace("_", " ").title(), df_final[col].mean())

            # Save full report
            METRICS_OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
            with open(METRICS_OUTPUT_FILE, "w", encoding="utf-8") as f:
                json.dump(evaluated_records, f, indent=4, ensure_ascii=False)
                
            logger.info("Detailed metrics saved successfully to %s", METRICS_OUTPUT_FILE)

        except asyncio.CancelledError:
            logger.warning("Evaluation manually halted via user async cancellation signature.")
        except Exception as e:
            logger.exception("An error occurred during the async RAGAS runner loop: %s", str(e))
            raise

    def run(self) -> None:
        """
        Entry point to bootstrap the asynchronous event loop safely.
        """
        try:
            asyncio.run(self._run_lifecycle())
        except KeyboardInterrupt:
            print("\n⚠️ Process safely terminated by user.")


if __name__ == "__main__":
    runner = RagasEvaluationRunner()
    runner.run()